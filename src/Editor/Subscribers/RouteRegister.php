namespace Zs\Foundation\Editor\Subscribers;

use Zs\Foundation\Editor\Controllers\UEditorController;
use Zs\Foundation\Routing\Abstracts\RouteRegister as AbstractRouteRegister;

/**
 * Class RouteRegister.
 */
class RouteRegister extends AbstractRouteRegister
{
    /**
     * Handle Route Register.
     */
    public function handle()
    {
        $this->router->group(['middleware' => ['web']], function () {
            $this->router->any('editor', UEditorController::class . '@handle');
        });
    }
}
